function getToolStatusLabel(toolName: string): string {
  switch (toolName) {
    case "get_dashboard_stats":
      return "Checking dashboard data...";
    case "get_persona_details":
      return "Looking up persona details...";
    case "get_sod_conflicts":
      return "Analyzing SOD conflicts...";
    case "get_mapping_status":
      return "Checking mapping progress...";
    case "trigger_auto_map":
      return "Starting auto-mapping...";
    case "trigger_sod_analysis":
      return "Running SOD analysis...";
    case "get_job_status":
      return "Checking job status...";
    case "get_calibration_summary":
      return "Reviewing confidence data...";
    case "create_role_mapping":
      return "Creating role mapping...";
    case "resolve_sod_conflict":
      return "Resolving SOD conflict...";
    case "accept_calibration_items":
      return "Accepting calibration items...";
    case "submit_for_review":
      return "Submitting assignments for review...";
    case "send_reminder":
      return "Sending reminder notifications...";
    default:
      return "Processing...";
  }
}
